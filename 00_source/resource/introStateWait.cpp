{
	// 自身の破棄
	delete this;
}

//============================================================
//	更新処理
//============================================================
void CIntroStateWait::Update(const float fDeltaTime)
{
	if (m_pIntro->WaitTime(fDeltaTime, 2.0f))
	{ // 待機終了した場合

		// 物語と状態を遷移させる
		m_pIntro->NextStory();
	}
}
