#ifndef _PLAYER_ITEM_H_
#define _PLAYER_ITEM_H_

//************************************************************
//	構造体定義
//************************************************************
// プレイヤーアイテム構造体
struct SPlayerItem
{
public:
	// 定数
	static constexpr int MAX_ITEM = 8;	// 最大アイテム数

	// デフォルトコンストラクタ
	SPlayerItem() { memset(&aItemIdx[0], 0, sizeof(aItemIdx)); }

	// デストラクタ
	~SPlayerItem() {}

	// メンバ変数
	int aItemIdx[MAX_ITEM];	// アイテムインデックス
};

#endif	// _PLAYER_ITEM_H_
