//============================================================
//	更新処理
//============================================================
void CIntroStateText::Update(const float fDeltaTime)
{
	if (!m_pContext->IsTextScroll())
	{ // 文字送りが終了した場合

		// 待機状態にする
		m_pContext->ChangeState(new CIntroStateWait);
	}
}
