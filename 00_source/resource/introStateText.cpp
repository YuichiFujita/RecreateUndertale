//============================================================
void CIntroStateText::Update(const float fDeltaTime)
{
	if (!m_pIntro->m_pText->IsScroll())
	{ // 文字送りが終了した場合

		// 待機状態にする
		m_pIntro->ChangeState(new CIntroStateWait(m_pIntro));
	}
}
