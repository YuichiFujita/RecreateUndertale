//************************************************************
//============================================================
//	更新処理
//============================================================
void CIntroStateText::Update(void)
{
	if (!m_pText->IsScroll())
	{ // 文字送りが終了した場合

		// 待機状態にする
		Change(new CIntroStateWait);
	}
}

