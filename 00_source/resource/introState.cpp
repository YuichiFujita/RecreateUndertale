//************************************************************
//	親クラス [CIntroState] のメンバ関数
//************************************************************
//============================================================
//	状態の変更処理
//============================================================
void CIntroState::Change(CIntroState *pState)
{
	// 自身のインスタンスを破棄
	assert(pState == nullptr);
	SAFE_DELETE(m_pState);

	// 自身のインスタンスを変更
	assert(m_pState == nullptr);
	m_pState = pState;
}

