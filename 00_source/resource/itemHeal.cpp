HRESULT CItemHeal::LoadSetup(std::ifstream* pFile, std::string& rString)
{
	// ファイルストリームが未設定の場合抜ける
	if (pFile == nullptr) { assert(false); return E_FAIL; }

	std::string str;	// 読込文字列
	if (rString == "HEAL")
	{
		*pFile >> str;		// ＝を読込
		*pFile >> m_nHeal;	// 回復量を読込
	}

	return S_OK;
}